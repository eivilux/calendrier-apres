<h2 class="text-xl font-bold mb-6">Le point RH à ne pas oublier en 2026 : la transparence salariale.</h2>
<p class="mb-4">La directive européenne arrive vite : <strong>juin 2026</strong>, chaque service RH devra être en conformité.</p>
<div class="mb-4 text-left max-w-xl">
	<p class="mb-2">Ce que je recommande à toutes les entreprises que j’accompagne — et que je fais depuis plus de 8 ans déjà :</p>
	<ul class="list-disc pl-6 mb-2">
		<li>mettre à jour (ou créer) des descriptifs de poste clairs</li>
		<li>catégoriser les postes de même valeur</li>
	</ul>
	<p>C’est la base d’une politique RH équitable, lisible et durable.</p>
</div>
